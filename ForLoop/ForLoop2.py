#Example 2: Looping a specific number of times with range()
#If you just want to do something 5 times, range(5) is your friend. It generates numbers from 0 up to (but not including) 5.

# range(5) gives us 0, 1, 2, 3, 4
#print(input("Enter number: "))
for number in range(7):
    print(f"Looping, cycle number {number}")
